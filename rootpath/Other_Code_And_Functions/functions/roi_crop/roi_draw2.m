function [coord0, mask0] = roi_draw2(back0,roi_labels0,num_neurons_x,num_neurons_y,movie)
close all
% THE FUNCTIONS ASKS TO DRAW THE ROIS IN THE INPUT BACKGROUND AND STORES THEM AS POLYGONS INTO A CELL
% STRUCTURE, AND AS MASKS INTO A 3-D MATRIX. If 'roi_labels' argument is not input, a single mask is done.
% Colormap is defined up to 14 rois

% INPUT:
% 'back' : 2D-image
% 'roi_labels': in the order in which they will be plot. Exmaple:
%     roi_labels = {'N1', 'N2', 'N3', 'N4',...
%     'N5', 'N6', 'N7', 'N8', 'N9', 'N10'...
%      'N11', 'N12', 'N13', 'N14' }';

% OUTPUT:
% 'coord': Cell-structure with one set of coordinates in each row.
% 'mask': 3D-matrix with one roi in each 3rd dimension

if nargin ==1
    nroi = 1;
    roi_labels0 = {'crop'};
    figure('units','normalized','outerposition',[0 0 1 1])
    fig1 = gca;
    imagesc(back0);colormap ('bone'), hold on

elseif nargin >=2
    nroi =  length(roi_labels0);
    figure('units','normalized','outerposition',[0 0 1 1])
    fig1 = gca;
    imagesc(back0);colormap ('bone'), hold on
    scatter(num_neurons_x,num_neurons_y,'r','filled')

end

roicolor = roi_colors();
xdim = size(back0,1);
ydim = size(back0,2);





for ii = 1:nroi
    figure(1)
     fig1 = gca;
    display(strcat('draw ROI:',roi_labels0{ii}, '-and Press any key to continue. Once you have drawn it, before pressing enter, you can adjust the points by simply dragging '))
    roi = images.roi.Circle(fig1 ,'Center',[num_neurons_x(ii) num_neurons_y(ii)],'Radius',10);
    roi.Color= roicolor(ii,:); 
% 
    addlistener(roi,'MovingROI',@allevents);
    addlistener(roi,'ROIMoved',@allevents);

    pause(0.1)
    coord0{ii,1} =  roi.Vertices;
    coord0{ii,1}(end+1,:) = coord0{ii,1}(1,:); % close the polygon
    mask0(:,:,ii) = poly2mask( roi.Vertices(:,1),  roi.Vertices(:,2), xdim, ydim); % create mask from polygo
    for iii = 1:size(movie,3)
        preview_movie(iii) = mean(nonzeros(movie(:,:,iii).*mask0(:,:,ii)));
    end
 
    figure(2);
    subplot(4,4,[1,2,5,6,9,10,13,14])
    plot(-preview_movie)
    title(roi_labels0(ii))


    prompt = 'SMOOTH TRACE: If you want to modify the value for the peaks type "Y", if you are satisfy type "N" : ';
    str = input(prompt,'s');
    if isempty(str)
        str = 'N';
        close
    else
    end

    while isequal(str,'Y')
    figure(2)
    subplot(4,4,[1,2,5,6,9,10,13,14])
    cla
    coord0{ii,1} =  roi.Vertices;
    coord0{ii,1}(end+1,:) = coord0{ii,1}(1,:); % close the polygon
    mask0(:,:,ii) = poly2mask( roi.Vertices(:,1),  roi.Vertices(:,2), xdim, ydim); % create mask from polygo
    for iii = 1:size(movie,3)
        preview_movie(iii) = mean(nonzeros(movie(:,:,iii).*mask0(:,:,ii)));
    end
    plot(-preview_movie)



        prompt = 'SMOOTH TRACE: If you want to modify the value for the peaks type "Y", if you are satisfy type "N" : ';
        str = input(prompt,'s');

        if isempty(str)
            str = 'N';
            close
        end

    end

    figure(2)
    subplot(4,4,[3,4,7,8,11,12,15,16]),hold on
    plot(-preview_movie,'color',roicolor(ii,:)), hold off





end

end
